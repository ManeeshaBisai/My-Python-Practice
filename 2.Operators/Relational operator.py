# Relational operator (<,>,==,!=,<=,>=)
"""Relational operators are used to compare Two values.
   They show the relationship between them and return True or False.
   Relational operators are also called Comparison operators."""
a = 50
b = 20
print(a == b) # False
print(a != b) # True
print(a <= b) # False
print(a >= b) # True
print(a < b) # False
print(a > b) # True