# Class & Object in Python 
""" Class is a blueprint for Creating Objects."""
# Creating class
class Student:
    name = "Maneesha Bisai"

# Creating objects(instance)
s1 = Student ()
print(s1)
print(s1.name)

# car class
class Car:
    color = "Black"
    brand = "Baleno"
Car1 = Car()
print(Car1.color)
print(Car1.brand)