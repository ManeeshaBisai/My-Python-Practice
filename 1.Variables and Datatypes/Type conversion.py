# Type conversion
"""It is automatically done by Python when needed.
   Python changes one data type to another by itself."""
a = 2
b = 4.5
sum = a + b
print(sum)
print(type(sum))

#Type Casting
"""It is manually done by the programmer to change the data type using
   Functions like int(),float(),or str()"""
a = 3.5
b = int("4")
sum = a + b
print(sum)
print(type(sum))