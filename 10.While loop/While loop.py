"""While loop = A while loop in python repeatedly runs a block of code as long as a condition is true."""
#print "hello" 5 times
count = 1
while count <= 5:
    print("hello")
    count += 1

# print numbers from 1 to 10
i = 1
while i <= 10:
    print(i)
    i += 1

# print numbers from 10 to 1
i = 10
while i >= 1:
    print(i)
    i -= 1